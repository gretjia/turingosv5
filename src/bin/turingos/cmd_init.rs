/// Genesis spec template selector for `turingos init --template`.
#[derive(Debug, Clone, Copy, PartialEq, Eq)]
enum Template {
    /// Lean proof market template (REAL-5 / REAL-12 baseline).
    Proof,
    /// Polymarket event-resolution template.
    Polymarket,
    /// Multi-agent arena template (REAL-12 BullTrader / BearTrader / Librarian roles).
    MultiAgent,
}

impl Template {
    fn parse(s: &str) -> Result<Self, String> {
        match s {
            "proof" => Ok(Self::Proof),
            "polymarket" => Ok(Self::Polymarket),
            "multi-agent" => Ok(Self::MultiAgent),
            other => Err(format!(
                "invalid template '{other}': expected one of: proof, polymarket, multi-agent"
            )),
        }
    }

    fn name(&self) -> &'static str {
        match self {
            Self::Proof => "proof",
            Self::Polymarket => "polymarket",
            Self::MultiAgent => "multi-agent",
        }
    }

    fn content(&self) -> &'static str {
        match self {
            Self::Proof => GENESIS_PROOF,
            Self::Polymarket => GENESIS_POLYMARKET,
            Self::MultiAgent => GENESIS_MULTI_AGENT,
        }
    }
}

/// Parsed arguments for `turingos init`.
#[derive(Debug)]
struct InitArgs {
    project: String,
    template: Template,
    force: bool,
}

/// Result of subcommand-level arg parsing: either parsed args, or a help
/// request that should print to stdout and exit 0.
enum ParsedInit {
    Args(InitArgs),
    HelpRequested,
}

fn parse_init_args(args: &[String]) -> Result<ParsedInit, String> {
    let mut project: Option<String> = None;
    let mut template: Option<Template> = None;
    let mut force = false;
    let mut iter = args.iter();

    while let Some(arg) = iter.next() {
        match arg.as_str() {
            "--project" => {
                let val = iter
                    .next()
                    .ok_or_else(|| "missing value for --project".to_string())?;
                project = Some(val.clone());
            }
            "--template" => {
                let val = iter
                    .next()
                    .ok_or_else(|| "missing value for --template".to_string())?;
                template = Some(Template::parse(val)?);
            }
            "--force" => {
                force = true;
            }
            "-h" | "--help" => {
                return Ok(ParsedInit::HelpRequested);
            }
            other => {
                return Err(format!("unrecognized argument: {other}"));
            }
        }
    }

    let project =
        project.ok_or_else(|| "missing required argument: --project <PATH>".to_string())?;
    let template = template.unwrap_or(Template::Proof);

    Ok(ParsedInit::Args(InitArgs {
        project,
        template,
        force,
    }))
}

