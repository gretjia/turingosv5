/// TRACE_MATRIX FC2-N16: init subcommand dispatch entry
pub(crate) fn run(args: &[String]) -> ExitCode {
    // Handle -h / --help first
    if args.iter().any(|a| a == "-h" || a == "--help") {
        print!("{}", FULL_HELP);
        return ExitCode::SUCCESS;
    }
    match parse_init_args(args) {
        Ok(ParsedInit::HelpRequested) => {
            print!("{}", FULL_HELP);
            ExitCode::SUCCESS
        }
        Ok(ParsedInit::Args(parsed)) => match cmd_init_inner(parsed) {
            Ok(()) => ExitCode::SUCCESS,
            Err(err) => {
                let code = err.exit_code();
                eprintln!("turingos: {err}");
                ExitCode::from(code)
            }
        },
        Err(msg) => {
            eprintln!("turingos init: {msg}");
            eprintln!();
            eprintln!("Run `turingos init --help` for usage.");
            ExitCode::from(2)
        }
    }
}
