//! `handover/directives/2026-05-11_TB_G_G1_2_OPTION_B_PLUS_RULING.md` §3.3):
//! `BatchContinuationManifest` — the architect-mandated fact-identity for
//! a multi-task batch. Without this manifest, "same batch" has no
//! canonical reconstruction; the dashboard would merely stitch
//! independent runs together.
//!
//! Architect §3.3 verbatim:
//!
//! > 新增 BatchContinuationManifest. 这个 manifest 进入 CAS, 并由最后
//! > 的 TerminalSummaryTx 或 BatchSummaryTx 引用. 否则 dashboard 仍然
//! > 只是把多个 runs 拼起来，不是真实 batch.
//!
//! Schema is stable across G1.2..G7 atoms (`schema_version =
//! "g1_2_v1"`). The G1.2-3 orchestrator writes the manifest to disk
//! as it runs; G1.2-4 promotes the in-memory shape to a strongly-typed
//! struct that can also land as a Generic CAS object (so future
//! `TerminalSummaryTx.batch_continuation_manifest_cid` can anchor it).
//!
//! Four gates SG-G1.2-4.1..G1.2-4.4:
//!  1. manifest_records_all_tasks_in_order
//!  2. manifest_head_chain_is_continuous
